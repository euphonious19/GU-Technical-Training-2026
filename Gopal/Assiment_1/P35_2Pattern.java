import java.util.Scanner;

public class P35_2Pattern {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in) ;
        System.out.print("Enter number : ");
        int num = sc.nextInt();

        for ( int i = 1 ; i <= num ; i++ ) {
            System.out.print(1);
        }
    }
}
